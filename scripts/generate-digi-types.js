import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const componentsPath = resolve('./node_modules/@designsystem-se/af/dist/types/components.d.ts');
const content = readFileSync(componentsPath, 'utf-8');

// Extract element names from LocalJSX.IntrinsicElements
const localJsxStart = content.indexOf('declare namespace LocalJSX {');
const localJsxSection = content.slice(localJsxStart);
const intrinsicStart = localJsxSection.indexOf('interface IntrinsicElements {');
const intrinsicEnd = localJsxSection.indexOf('\n    }', intrinsicStart);
const intrinsicContent = localJsxSection.slice(intrinsicStart, intrinsicEnd);

const elements = [...intrinsicContent.matchAll(/"(digi-[^"]+)":/g)].map((m) => m[1]);

const output = `// Auto-generated by scripts/generate-digi-types.js — do not edit manually.
// Run \`node scripts/generate-digi-types.js\` to regenerate after updating @designsystem-se/af.
import type { JSX } from '@designsystem-se/af';

type CamelToKebab<S extends string> = S extends \`\${infer F}\${infer R}\`
\t? F extends Uppercase<F>
\t\t? F extends Lowercase<F>
\t\t\t? \`\${F}\${CamelToKebab<R>}\`
\t\t\t: \`-\${Lowercase<F>}\${CamelToKebab<R>}\`
\t\t: \`\${F}\${CamelToKebab<R>}\`
\t: S;

type DigiPropValue<T> = T extends boolean ? T : T extends string | number | bigint ? \`\${T}\` : T;

type LowercaseFirst<S extends string> = S extends \`\${infer F}\${infer R}\` ? \`\${Lowercase<F>}\${R}\` : S;

type DigiProps<T> = {
\t[K in keyof T as K extends \`on\${infer E}\` ? \`on\${LowercaseFirst<E>}\` : CamelToKebab<string & K>]?: DigiPropValue<T[K]>;
};

declare module 'svelte/elements' {
\tinterface SvelteHTMLElements {
${elements.map((el) => `\t\t'${el}': DigiProps<JSX.IntrinsicElements['${el}']>;`).join('\n')}
\t}
}
`;

writeFileSync('./src/digi-elements.d.ts', output);
console.log(`Generated types for ${elements.length} elements.`);
